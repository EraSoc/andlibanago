package com.link2loyalty.bwigomdlib.models2.user;

public class LovReward {

    String clvrec;
    String clvreg;
    String reg;
    String niv;
    String can;
    int tip;
    int cam;
    String imglog;

    public LovReward() { }



    public String getClvrec() {
        return clvrec;
    }

    public void setClvrec(String clvrec) {
        this.clvrec = clvrec;
    }

    public String getClvreg() {
        return clvreg;
    }

    public void setClvreg(String clvreg) {
        this.clvreg = clvreg;
    }

    public String getReg() {
        return reg;
    }

    public void setReg(String reg) {
        this.reg = reg;
    }

    public String getNiv() {
        return niv;
    }

    public void setNiv(String niv) {
        this.niv = niv;
    }

    public String getCan() {
        return can;
    }

    public void setCan(String can) {
        this.can = can;
    }

    public int getTip() {
        return tip;
    }

    public void setTip(int tip) {
        this.tip = tip;
    }

    public int getCam() {
        return cam;
    }

    public void setCam(int cam) {
        this.cam = cam;
    }

    public String getImglog() {
        return imglog;
    }

    public void setImglog(String imglog) {
        this.imglog = imglog;
    }
}
